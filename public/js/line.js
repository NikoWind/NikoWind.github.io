$(function() {
  $('.cloudBox').mouseover(function() {
    $(this)
      .find('.top,.bottom')
      .stop()
      .animate({ width: '338px' }, 618)
    $(this)
      .find('.right,.left')
      .stop()
      .animate({ height: '498px' }, 618)
    $(this).addClass('color')
  })
  $('.cloudBox').mouseout(function() {
    $(this)
      .find('.top,.bottom')
      .stop()
      .animate({ width: '0px' })
    $(this)
      .find('.right,.left')
      .stop()
      .animate({ height: '0px' })
    $(this).removeClass('color')
  })
  $('.dau-list .topColor').mouseover(function() {
    $(this)
      .addClass('color')
      .stop()
      .animate({ top: -10 }, 300)
    $(this)
      .children('.dau-title')
      .addClass('color')
  })
  $('.dau-list .topColor').mouseout(function() {
    $(this)
      .removeClass('color')
      .stop()
      .animate({ top: 0 }, 300)
    $(this)
      .children('.dau-title')
      .removeClass('color')
  })
  $('.healthBox').mouseover(function() {
    $(this)
      .find('.top,.bottom')
      .stop()
      .animate({ width: '202px' }, 300)
    $(this)
      .find('.right,.left')
      .stop()
      .animate({ height: '164px' }, 300)
  })
  $('.healthBox').mouseout(function() {
    $(this)
      .find('.top,.bottom')
      .stop()
      .animate({ width: '0px' })
    $(this)
      .find('.right,.left')
      .stop()
      .animate({ height: '0px' })
  })
})
