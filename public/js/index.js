$(function() {
  $('.item-poc img')
    .mouseenter(function() {
      $(this)
        .css('z-index', '2')
        .animate({ width: '+=50px', height: '+=50px', top: '-=25px', left: '-=25px' }, 300)
    })
    .mouseleave(function() {
      $(this).animate({ width: '-=50px', height: '-=50px', top: '+=25px', left: '+=25px', 'z-index': '0' }, 150)
    })
  $('.advantage-li').mouseover(function() {
    $(this)
      .addClass('color')
      .stop()
      .animate({ top: -10 }, 200)
    $(this)
      .children('.advantage-weight')
      .addClass('color')
  })
  $('.advantage-li').mouseout(function() {
    $(this)
      .removeClass('color')
      .stop()
      .animate({ top: 0 }, 200)
    $(this)
      .children('.advantage-weight')
      .removeClass('color')
  })
  $('.sence .sence1')
    .mouseenter(function() {
      $(this)
        .find('.item-icon')
        .css('z-index', '2')
        .animate({ width: '+=10px', height: '+=10px', top: '-=5px', left: '-=5px' }, 0)
      $(this)
        .css({ fontSize: '21px' })
        .animate({}, 0)
    })
    .mouseleave(function() {
      $(this)
        .find('.item-icon')
        .animate({ width: '-=10px', height: '-=10px', top: '+=5px', left: '+=5px', 'z-index': '0' }, 0)
      $(this)
        .css({ fontSize: '19px' })
        .animate({}, 0)
    })
})
